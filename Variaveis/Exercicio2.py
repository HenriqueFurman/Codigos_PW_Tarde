# Variáveis

numero_1 = float(0);
numero_2 = float(0);
numero_3 = float(0);
media = float(0);

# Explicação
print('O algoritmo calculará a média dos 3 números digitados.');

# Input
numero_1 = float(input('Digite: '));
numero_2 = float(input('Digite: '));
numero_3 = float(input('Digite: '));

# Processamento
media = (numero_1 + numero_2 + numero_3) / 3;

# Saída de Dados (Output)
print(f'A média dos números digitados é: {media}');
